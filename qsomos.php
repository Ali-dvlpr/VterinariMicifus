<h2 class="QuienesSomos">Quiénes Somos</h2>
<p class="Descripcion">
    Veterinaria Micifús abrió sus puertas hace una década con el propósito de brindar atención médica y cuidados
    especializados a perros.
    A lo largo de los años, hemos crecido y evolucionado para atender no solo a perros, sino también a gatos, caballos,
    ganado bovino y caprino.
</p>
<p class="Descripcion">
    Actualmente, no solo ofrecemos servicios veterinarios de alta calidad, sino también una amplia variedad de productos
    para el cuidado,
    la alimentación y la salud de los animales que forman parte de nuestra familia extendida.
</p>

<h3 class="MisionVision">Misión</h3>
<p class="Descripcion">
    Nuestra misión es proporcionar atención veterinaria de excelencia, contribuyendo al bienestar y salud de las
    mascotas y animales de producción
    mediante servicios personalizados, innovadores y profesionales. Nos comprometemos a brindar productos de calidad que
    satisfagan las necesidades
    de nuestros clientes, asegurando un cuidado integral y fomentando el vínculo humano-animal.
</p>

<h3 class="MisionVision">Visión</h3>
<p class="Descripcion">
    Ser la veterinaria líder en la región, reconocida por nuestra dedicación, calidad de servicio y compromiso con el
    bienestar animal.
    Aspiramos a seguir creciendo como una empresa referente, ampliando nuestras capacidades para atender con excelencia
    a mascotas y animales de producción,
    mientras promovemos una cultura de cuidado y respeto hacia los animales.
</p>